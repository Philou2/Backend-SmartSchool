namespace App\Repository\School\Schooling\Registration;

use App\Entity\School\Schooling\Registration\Student;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Student>
 *
 * @method Student|null find($id, $lockMode = null, $lockVersion = null)
 * @method Student|null findOneBy(array $criteria, array $orderBy = null)
 * @method Student[]    findAll()
 * @method Student[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class StudentRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Student::class);
    }

    public function save(Student $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Student $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }


    /**
     * @return int Returns the count of Student objects for the current year
     */
    public function countStudentsForCurrentYear($currentYear): int
    {

        return $this->createQueryBuilder('s')
            ->select('count(s.id)')
            ->andWhere('s.status = :status')
            ->andWhere('s.year = :currentYear')
            ->setParameter('status', 'registered')
            ->setParameter('currentYear', $currentYear)
            ->getQuery()
            ->getSingleScalarResult();
    }

